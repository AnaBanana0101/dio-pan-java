public class BitWise {
    public static void main(String[] args) {

    int n1 = 89; 
    int n2 = 60; 

    System.out.println(n1 & n2); // e
    System.out.println(n1 | n2); //ou
    System.out.println(n1 ^ n2); //ou-exclusivo 

    //a operação é realizada em nível binário, onde o numero original é convertido em binário 
    //e realizado as operação bit por bit (número por número), comparando os 2.

    }
}
