public class Operacao {
    
    public static void main(String[] arg) {
        int numero = 5; 

        numero = - numero;
        System.out.println(numero);
        
        //+ - * / são operadores aritméticos, para realizar conversao 
        // necessita de uma multiplicação. 
        numero = numero * - 1;
        System.out.println(numero);


    }
}